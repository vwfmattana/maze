public class SLLNode
{
	//******************************************************************************************************
	//												DECLARATIONS
	//******************************************************************************************************
	public Block info;
	public SLLNode next;
	
	//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
	//                                             DEFAULT CONSTRUCTOR
	//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
	public SLLNode()
	{	
		next = null;
	}
	
	//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
	//                                             CONSTRUCTORS
	//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
	public SLLNode(Block element)
	{
		info = element;
		next = null;
	}
	
	public SLLNode(Block element, SLLNode n)
	{
		info = element;
		next = n;
	}

	//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
	//                                            ACCESSORS & MUTATORS
	//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
	public Block getInfo()
	{
		return info;
	}
	
	public void setInfo(Block element)
	{
		info = element;
	}
	
	public SLLNode getNext()
	{
		return next;
	}
	
	public void setNext(SLLNode newNext)
	{
		next = newNext;
	}
	
}// END OF SLLNode<T> CLASS